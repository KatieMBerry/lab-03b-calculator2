//Function declarations live here and are exported:

export function addTwoNums(num1, num2) {
    return num1 + num2;
}

export function subtractTwoNums(num1, num2) {
    return num1 - num2;
}

export function multiplyTwoNums(num1, num2) {
    return num1 * num2;
}

export function divideTwoNums(num1, num2) {
    return num1 / num2;
}
